import logging
from langchain_core.prompts import ChatPromptTemplate

logger = logging.getLogger(__name__)

class CypherRetriever:
    def __init__(self, neo4j_driver, llm_runnable):
        self.driver = neo4j_driver
        self.llm = llm_runnable
        self.schema_str = self._get_schema()

    def _get_schema(self):
        
        query = "CALL apoc.meta.schema() YIELD value RETURN value"
        try:
            with self.driver.session() as session:
                result = session.run(query).single()
                return str(result['value']) if result else ""
        except Exception:
            return ""

    def _generate_cypher(self, user_query):
        
        system_prompt = """
            You are an expert Neo4j Developer translating user questions into Cypher to answer questions about data provenance (PROV-O model).
            Convert the user's question based on the schema provided.

            Schema:
            {schema}

            Instructions:
            1. Use ONLY the provided node labels, relationship types, and property keys.
            2. **ALWAYS LIMIT RESULTS**: If the user asks for a list (e.g., "Find all..."), append `LIMIT 50` to prevent system overload.
            3. **Case Insensitivity**: When filtering by name or text, always use `toLower()` or `(?i)` regex to ensure partial/case-insensitive matching.
            4. **Read-Only**: Do not use CREATE, MERGE, SET, or DELETE. Do not use GDS projections unless explicitly requested.
            5. **Return Format**:
            - If counting: `RETURN count(n)`
            - If listing: `RETURN n.name, n.id` (or specific properties)
            - If finding paths: `RETURN p`

            Examples:

            # Find activities with "processing" in the name (Case Insensitive)
            MATCH (a:Activity)
            WHERE toLower(a.name) CONTAINS "processing"
            RETURN a
            LIMIT 50

            # How many entities were generated by the first activity?
            MATCH (first:Activity)
            WHERE NOT ()-[:NEXT]->(first)
            MATCH (e:Entity)-[:WAS_GENERATED_BY]->(first)
            RETURN count(e)

            # What are the inputs for the activity 'Analysis_V1'?
            MATCH (a:Activity {{function_name: 'Analysis_V1'}})<-[:USED]-(e:Entity)
            RETURN e.name, e.type
            LIMIT 50

            # Trace the lineage: What derived from Entity 'E-123'?
            MATCH p=(start:Entity {{id: 'E-123'}})<-[:WAS_DERIVED_FROM*1..5]-(end:Entity)
            RETURN end.name
            LIMIT 50

            # Find the full history of Entity X
            MATCH p=(e:Entity {{name: 'X'}})-[:WAS_DERIVED_FROM*]->(ancestor)
            RETURN p
            LIMIT 50

            Do not include any explanations or apologies in your responses. Output ONLY the Cypher string.
            """
        
        prompt = ChatPromptTemplate.from_messages([
            ("system", system_prompt),
            ("human", "{input}")
        ])
        
        chain = prompt | self.llm
        response = chain.invoke({"schema": self.schema_str, "input": user_query})
        return response.content.replace("```cypher", "").replace("```", "").strip()

    def retrieve(self, user_query):
        """
        Returns a dict with two keys:
        - 'nodes': List of node objects.
        - 'facts': List of strings/values.
        """
        cypher_query = self._generate_cypher(user_query)
        logger.info(f"Generated Cypher: {cypher_query}")

        nodes = []
        facts = []

        try:
            with self.driver.session() as session:
                result = session.run(cypher_query)
                
                # Get the keys (column names) and values together
                for record in result:
                    keys = record.keys()
                    values = record.values()
                    
                    for key, val in zip(keys, values):
                        # Check if it's a Graph Node
                        if hasattr(val, 'element_id') or hasattr(val, 'id'):
                            node_id = val.element_id if hasattr(val, 'element_id') else val.id
                            nodes.append({
                                "id": node_id,
                                # Add the label to the content for better context
                                "content": f"{key} ({list(val.labels)[0] if hasattr(val, 'labels') else 'Node'}): {str(dict(val.items()))}",
                                "source": "cypher_node"
                            })
                        
                        # Check if it's a Path
                        elif hasattr(val, 'nodes'): 
                            for n in val.nodes:
                                node_id = n.element_id if hasattr(n, 'element_id') else n.id
                                nodes.append({
                                    "id": node_id,
                                    "content": str(dict(n.items())),
                                    "source": "cypher_path"
                                })
                        
                        # Check if it's data (Facts)
                        else:
                            facts.append(f"{key}: {val}")
                            
        except Exception as e:
            logger.error(f"Cypher fail: {e}")
            # Return empty lists but include the query that failed
            return {"nodes": [], "facts": [], "cypher_query": cypher_query}
            
        # Return the query so the formatter can use it
        return {"nodes": nodes, "facts": facts, "cypher_query": cypher_query}